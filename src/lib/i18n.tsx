type I18nContextValue = {
  language: LanguageCode;
  languageMeta: LanguageMeta;
  languages: LanguageMeta[];
  setLanguage: (language: LanguageCode) => void;
  t: (key: TranslationKey) => string;
};

const I18nContext = createContext<I18nContextValue | null>(null);

function getInitialLanguage(): LanguageCode {
  if (typeof window === "undefined") return "en";
  const saved = window.localStorage.getItem(STORAGE_KEY);
  if (saved && saved in dictionaries) return saved as LanguageCode;
  const browserLanguage = window.navigator.language.slice(0, 2) as LanguageCode;
  return browserLanguage in dictionaries ? browserLanguage : "en";
}

export function I18nProvider({ children }: { children: ReactNode }) {
  const [language, setLanguageState] = useState<LanguageCode>("en");
  const languageMeta = languages.find((item) => item.code === language) ?? languages[0];

  const setLanguage = useCallback((nextLanguage: LanguageCode) => {
    setLanguageState(nextLanguage);
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, nextLanguage);
    }
  }, []);

  useEffect(() => {
    setLanguageState(getInitialLanguage());
  }, []);

  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.lang = language;
    document.documentElement.dir = languageMeta.dir;
  }, [language, languageMeta.dir]);

  const t = useCallback(
    (key: TranslationKey) => dictionaries[language]?.[key] ?? dictionaries.en[key] ?? key,
    [language],
  );

  const value = useMemo(
    () => ({ language, languageMeta, languages, setLanguage, t }),
    [language, languageMeta, setLanguage, t],
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used inside I18nProvider");
  }
  return context;
}
